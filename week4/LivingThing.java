package week4;
public class LivingThing {

    // attributes
    private String name;
    private int energy;

    // constructor
    public LivingThing(String nameIn) {
        name = nameIn;
        energy = 100;
    }

    

    // methods
    
    public void age() {
        age = age + 1;
    }


    public int getEnergy() {
        return energy;
    }

}